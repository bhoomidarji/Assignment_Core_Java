package Assignment;

public class W_A_J_P_in_java_to_display_the_first_10_natural_numbers_using_while_loop {

    public static void main(String[] args) {
        int n=1; 
        System.out.println("First ten natural numbers : ");

        
        while (n<= 10)
        {
            System.out.println(n);
            n++;
        }
    }
}
